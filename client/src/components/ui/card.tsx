import * as React from "react"
import { useRef, useCallback } from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, children, ...props }, ref) => {
  const cardRef = useRef<HTMLDivElement>(null);
  const glowRef = useRef<HTMLDivElement>(null);

  const handleMouseMove = useCallback((e: React.MouseEvent<HTMLDivElement>) => {
    const card = cardRef.current;
    if (!card) return;
    const rect = card.getBoundingClientRect();
    card.style.setProperty('--x', `${e.clientX - rect.left}px`);
    card.style.setProperty('--y', `${e.clientY - rect.top}px`);
  }, []);

  return (
    <div className="glow-card-wrapper h-full">
      <div
        ref={(node) => {
          (cardRef as React.MutableRefObject<HTMLDivElement | null>).current = node;
          if (typeof ref === 'function') ref(node);
          else if (ref) (ref as React.MutableRefObject<HTMLDivElement | null>).current = node;
        }}
        className={cn(
          "shadcn-card glow-card rounded-xl border bg-card border-card-border text-card-foreground shadow-sm",
          className
        )}
        onMouseMove={handleMouseMove}
        {...props}
      >
        {children}
        <div className="glow-card-noise" />
        <div ref={glowRef} className="glow-card-light" />
      </div>
    </div>
  );
});
Card.displayName = "Card"

